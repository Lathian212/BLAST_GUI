from tkinter import *
import re

class AutocompleteEntry(Entry):
    def __init__(self, autocompleteList, *args, **kwargs):

        # Listbox length
        if 'listboxLength' in kwargs:
            self.listboxLength = kwargs['listboxLength']
            del kwargs['listboxLength']
        else:
            self.listboxLength = 20

        # Custom matches function
        if 'matchesFunction' in kwargs:
            self.matchesFunction = kwargs['matchesFunction']
            del kwargs['matchesFunction']
        else:
            def matches(fieldValue, acListEntry):
                pattern = re.compile('.*' + re.escape(fieldValue) + '.*', re.IGNORECASE)
                return re.match(pattern, acListEntry)
                
            self.matchesFunction = matches

        
        Entry.__init__(self, *args, **kwargs)
        self.focus()

        self.autocompleteList = autocompleteList
        
        # Can pass in textvariable which is StringVar in constructor because parent class has textvariable
        #  to get a handle on text in Entry box
        self.var = self["textvariable"]
        if self.var == '':
            self.var = self["textvariable"] = StringVar()
        """ Tkinter variables have a method called trace to add an observer, 
        so the callback function is called when the value changes. In this case Entry box """
        self.var.trace('w', self.changed)
        # Maybe should take out right arrow it's none intuitive.
        self.bind("<Right>", self.selection)
        self.bind('<Return>', self.selection)
        self.bind("<Up>", self.moveUp)
        self.bind("<Down>", self.moveDown)
        
        self.listboxUp = False

    def changed(self, name, index, mode):
        #Below is to cut of up and down arrow characters leaking through in mac os. 
        if self.var.get()[-1] == '\uf700' or self.var.get()[-1] == '\uf701' :
            untrimmed = self.var.get()
            length = len(untrimmed)
            self.var.set(untrimmed[0:(length-1)])
            return
        
        if self.var.get() == '':
            if self.listboxUp:
                self.listbox.destroy()
                self.listboxUp = False
        else:
            words = self.comparison()
            if words:
                if not self.listboxUp:
                    self.listbox = Listbox(width=self["width"], height=self.listboxLength)
                    self.listbox.bind("<Button-1>", self.selection)
                    self.listbox.bind("<Right>", self.selection)
                    """self.winfo_x/y Return the x, y coordinates of the upper left corner of this widget
                    in the parent. This widget is the Entry box. self.winfo_height() returns height of Entry box.
                    So below statement places a list box just below Entry box."""
                    self.listbox.place(x=self.winfo_x(), y=self.winfo_y() + self.winfo_height())
                    self.listboxUp = True
                
                self.listbox.delete(0, END)
                #Inserts all the regex matches into the list. 
                for w in words:
                    self.listbox.insert(END,w)
            else:
                if self.listboxUp:
                    self.listbox.destroy()
                    self.listboxUp = False
        
# Perhaps once selection made it returns something to main module?a        
    def selection(self, event):
        if self.listboxUp:
            self.var.set(self.listbox.get(ACTIVE))
            self.listbox.destroy()
            self.listboxUp = False
            self.icursor(END)

    def moveUp(self, event):
        if self.listboxUp:
            """Curselection() Return the indices of currently selected item."""
            if self.listbox.curselection() == ():
                index = '0'
            else:
                """ Return type as a tuple so slice to get string """ 
                index = self.listbox.curselection()[0]
                
            if index != '0':
                """ self.listbox.select_clear(first, last) Clear the selection from FIRST to LAST (included).
                Otherwise the list item remains selected. """
                self.listbox.selection_clear(first=index)
                """Decrease index by one because scrolling up """
                index = str(int(index) - 1)
                """ self.listbox.see(index) = scroll such that index is visible. """
                self.listbox.see(index) # Scroll!
                """ self.listbox.select_set(first, last)   Set the selection from FIRST to LAST 
                (included) without changing the currently selected elements. Here index was decremented"""
                self.listbox.selection_set(first=index)
                """ Sets the active element to the one indicated by index. If index is outside the range of elements in the listbox 
                then the closest element is activated. The active element is drawn as specified by -activestyle 
                when the widget has the input focus, and its index may be retrieved with the index active. """
                self.listbox.activate(index)

    def moveDown(self, event):
        
        if self.listboxUp:
            if self.listbox.curselection() == ():
                index = '-1'
            else:
                index = self.listbox.curselection()[0]
                
            if index != END:                        
                self.listbox.selection_clear(first=index)
                index = str(int(index) + 1)
                
                self.listbox.see(index) # Scroll!
                self.listbox.selection_set(first=index)
                self.listbox.activate(index) 

    def comparison(self):
        return sorted ([ w for w in self.autocompleteList if self.matchesFunction(self.var.get(), w) ])

